public class shift_opt {
        public static void main(String[] args){
            int x = -20;

            //Left Shift Operator
            System.out.println("Left Shift Operator : "+ (x << 2));

            //Right Shift Operator
            System.out.println("Right Shift Operator : "+ (x >> 2));
            System.out.println("Right Shift Operator : "+ (x >>> 2));

}
}