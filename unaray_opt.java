public class unaray_opt {
    public static void main(String[] args){
        int x = 40;

        // Increment Unary Operator (Work Only One Operend)
        System.out.println("Post increment value of 'X' : " + x++);
        System.out.println("Pre  increment value of 'X' : " + ++x);

        // Decerement Unary Operator (Work Only One Operend)
        System.out.println("Post decrement value of 'X' : " + x--);
        System.out.println("Pre  decrement value of 'X' : " + --x);

}
}
